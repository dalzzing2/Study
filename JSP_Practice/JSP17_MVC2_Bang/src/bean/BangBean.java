package bean;

public class BangBean {
	private int bang_idx;
	private String bang_name;
	private String bang_content;
	
	public int getBang_idx() {
		return bang_idx;
	}
	public void setBang_idx(int bang_idx) {
		this.bang_idx = bang_idx;
	}
	public String getBang_name() {
		return bang_name;
	}
	public void setBang_name(String bang_name) {
		this.bang_name = bang_name;
	}
	public String getBang_content() {
		return bang_content;
	}
	public void setBang_content(String bang_content) {
		this.bang_content = bang_content;
	}
	
	
}
